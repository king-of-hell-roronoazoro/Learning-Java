public class printANDprintln {
    public static void main(String[] args){
        System.out.println("if you put println, it will print the text and move to the next line");
        System.out.println("like this");
        System.out.print("but if you put print, it will print the text but stay on the same line");
        System.out.println("like this");
        System.out.println("or you can put /n to do the same thing as println");
        System.out.print("like this\n");
        System.out.print("see this is in the next line");
    }}